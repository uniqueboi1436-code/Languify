// Function to get language name by code
function getLanguageName(code) {
  const lang = LANGUAGES.find(l => l.code === code);
  return lang ? lang.name : code;
}

// Function to filter languages based on search
function filterLanguages(searchTerm) {
  if (!searchTerm.trim()) {
    return LANGUAGES;
  }
  
  const term = searchTerm.toLowerCase();
  return LANGUAGES.filter(lang => 
    lang.name.toLowerCase().includes(term) || 
    lang.code.toLowerCase().includes(term) ||
    lang.region.toLowerCase().includes(term)
  );
}

// Function to populate select dropdown with filtered languages
function populateLanguageSelect(selectElement, searchTerm = '') {
  const filtered = filterLanguages(searchTerm);
  const currentValue = selectElement.value;
  
  selectElement.innerHTML = '';
  
  filtered.forEach(lang => {
    const option = document.createElement('option');
    option.value = lang.code;
    option.textContent = lang.name;
    selectElement.appendChild(option);
  });
  
  // Try to maintain the previously selected value if it exists in filtered results
  if (filtered.some(l => l.code === currentValue)) {
    selectElement.value = currentValue;
  }
}

